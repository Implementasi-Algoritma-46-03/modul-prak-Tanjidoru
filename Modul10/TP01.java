public class TP01 {

    public static void main(final String[] args) {
        // Kerjakan soalnya di sini
        for (int i = 5; i >= 1; i--) {
            for (int a = 1; a <= i; a++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
       
    

